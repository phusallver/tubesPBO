    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try{
            FileInputStream fileBuku = new FileInputStream("file_buku.txt");
            ObjectInputStream inputBuku = new ObjectInputStream(fileBuku);
            
            FileInputStream fileAnggota = new FileInputStream("file_anggota.txt");
            ObjectInputStream inputAnggota = new ObjectInputStream(fileAnggota);
            
            app.SetDaftarBuku((ArrayList<Buku>)inputBuku.readObject());
            app.SetDaftarAnggota((ArrayList<Anggota>)inputAnggota.readObject());
            
            inputBuku.close();
            inputAnggota.close();
            fileBuku.close();
            fileAnggota.close();
        }catch(Exception e){
            System.out.println(e.toString());
            System.exit(1);
        }
    }//GEN-LAST:event_formWindowOpened

