    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        // TODO add your handling code here:
        try{
            FileOutputStream fileBuku = new FileOutputStream("file_buku.txt");
            ObjectOutputStream outputBuku = new ObjectOutputStream(fileBuku);
            
            FileOutputStream fileAnggota = new FileOutputStream("file_anggota.txt");
            ObjectOutputStream outputAnggota = new ObjectOutputStream(fileAnggota);
            
            outputBuku.writeObject(app.GetDaftarBuku()); //new ArrayList<Buku>()             app.GetDaftarBuku()
            outputAnggota.writeObject(app.GetDaftarAnggota()); //new ArrayList<Anggota>()    app.GetDaftarAnggota()
            
            outputBuku.close();
            fileBuku.close();
            outputAnggota.close();
            fileAnggota.close();
        }catch(Exception e){
            System.out.println(e.toString());
            System.exit(1);
        }
    }//GEN-LAST:event_formWindowClosing

