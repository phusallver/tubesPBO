    private void tampilkanBukuButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tampilkanBukuButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelAnggota = (DefaultTableModel) tabelAnggotaTable.getModel();
        int rowAnggota = tabelAnggotaTable.getSelectedRow();
        String peminjam = modelAnggota.getValueAt(rowAnggota, 1).toString()+ " " + modelAnggota.getValueAt(rowAnggota, 2).toString();
        Anggota anggota = app.GetDaftarAnggota().get(app.CariAnggota(peminjam));
        if (!anggota.GetBukuPinjaman().isEmpty()){
            judulBuku1Label.setText("Buku : "+anggota.GetBukuPinjaman().get(0).GetJudulBuku());
            waktuKembali1Label.setText("Waktu Kembali : "+anggota.GetBukuPinjaman().get(0).GetWaktuKembali());
        }else{
            judulBuku1Label.setText("Buku : -");
            waktuKembali1Label.setText("Waktu Kembali : -");
        }
        if (anggota.GetBukuPinjaman().size()>1){
            judulBuku2Label.setText("Buku : "+anggota.GetBukuPinjaman().get(1).GetJudulBuku());
            waktuKembali2Label.setText("Waktu Kembali : "+anggota.GetBukuPinjaman().get(1).GetWaktuKembali());
        }else{
            judulBuku2Label.setText("Buku : -");
            waktuKembali2Label.setText("Waktu Kembali : -");
        }
        if (anggota.GetBukuPinjaman().size()>2){
            judulBuku3Label.setText("Buku : "+anggota.GetBukuPinjaman().get(2).GetJudulBuku());
            waktuKembali3Label.setText("Waktu Kembali : "+anggota.GetBukuPinjaman().get(2).GetWaktuKembali());
        }else{
            judulBuku3Label.setText("Buku : -");
            waktuKembali3Label.setText("Waktu Kembali : -");
        }
    }//GEN-LAST:event_tampilkanBukuButtonActionPerformed

    private void tombolKembalikanBukuButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolKembalikanBukuButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelAnggota = (DefaultTableModel) tabelAnggotaTable.getModel();
        int rowAnggota = tabelAnggotaTable.getSelectedRow();
        
        String peminjam = modelAnggota.getValueAt(rowAnggota, 1).toString()+ " " + modelAnggota.getValueAt(rowAnggota, 2).toString();
        Anggota anggota = app.GetDaftarAnggota().get(app.CariAnggota(peminjam));
        
        if (tabelAnggotaTable.getSelectedRow() != -1 && (cekBuku1.isSelected()||cekBuku2.isSelected()||cekBuku3.isSelected())){
            if(cekBuku3.isSelected()&&anggota.GetBukuPinjaman().size()>2){
                app.KembalikanBuku(peminjam, anggota.GetBukuPinjaman().get(2).GetJudulBuku());
            }
            if(cekBuku2.isSelected()&&anggota.GetBukuPinjaman().size()>1){
                app.KembalikanBuku(peminjam, anggota.GetBukuPinjaman().get(1).GetJudulBuku());
            }
            if(cekBuku1.isSelected()&&anggota.GetBukuPinjaman().size()>0){
                app.KembalikanBuku(peminjam, anggota.GetBukuPinjaman().get(0).GetJudulBuku());
            }
            JOptionPane.showMessageDialog(this,"Semua buku telah dikembalikan.");
        }else{
            JOptionPane.showMessageDialog(this,"Silahkan pilih peminjam dan buku yang akan dikembalikan.");
        }
    }//GEN-LAST:event_tombolKembalikanBukuButtonActionPerformed

